int toupper ( int c )
{
    if ( c < 'a' || c > 'z' )
    {
        return c;
    }

    return c - 'a' + 'A';
}

int tolower ( int c )
{
    if ( c < 'A' || c > 'Z' )
    {
        return c;
    }

    return c - 'A' + 'a';
}

int isalnum ( int c )
{
    return ( c >= '0' && c <= '9' ) || ( c >= 'a' && c <= 'z' ) || ( c >= 'A' && c <= 'Z' );
}

int isalpha ( int c )
{
    return ( c >= 'a' && c <= 'z' ) || ( c >= 'A' && c <= 'Z' );
}

int isdigit ( int c )
{
    return ( c >= '0' && c <= '9' );
}

int islower ( int c )
{
    return ( c >= 'a' && c <= 'z' );
}

int isprint ( int c )
{
    return ( c >= 0x20 && c <= 0x7e );
}

int isspace ( int c )
{
    return ( c == '\x20' || c == '\t' );
}

int isupper ( int c )
{
    return ( c >= 'A' && c <= 'Z' );
}

int isxdigit ( int c )
{
    return ( c >= '0' && c <= '9' ) || ( c >= 'a' && c <= 'f' ) || ( c >= 'A' && c <= 'F' );
}

int isascii ( int c )
{
    return c >= 0 && c <= 127;
}

int isblank ( int c )
{
    return c == '\x20' || c == '\t';
}
